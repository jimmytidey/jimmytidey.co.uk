            <h2 id='hypothesis'>Abstract</h2>
            <p>This project investigates using ‘digital exhaust’ as a means of binding, informing and improving communities.</p> 

            <p>Many communities publish a stream of digital content. For example, an office might have an internal mailing list, employees might have Twitter or Tumblr accounts, there might also be a Facebook group for the company. Collectively, this content is ‘digital exhaust’</p>

            <p>           In the context of a geographic community, a high street also has a similar stream of digital exhaust: the Twitter account of the Cafe, the Facebook page of the pub, a blogger who lives nearby, or tweets that mention the farmer’s market. 

            </p>         

            <p>
                This data can be collected, processed and presented back to the underlying community. My research question is whether this representation can improve the level of cohesion or increase group activity. A further question is what kinds of processing and representation are most effective. 
            </p>          
            <p>
               Community is a broad term, this work will focus upon “goal oriented” communities, ones where the participants are bound by a desire to further a particular outcome. In the example of the office, goals are defined by business objectives. In the example of a high street, actions such as responding to planning consultations are taken as goals.
           </p>
           <p>The research question will be addressed by using prototypes to test the effectiveness of various interventions. Where possible, standardised instruments of group cohesion will be used to evaluate outcomes. </p> 

