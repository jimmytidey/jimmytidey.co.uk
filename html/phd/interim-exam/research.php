			<h3 id='step_1'>1 - Finding data for presentation.</h3>

			<p>A fundamental consideration is the availability of data to for presentation</p> 

			<p>Two prototype versions of the system, demonstrated at FACT and Kensington & Chelsea Council Staff cafe were both able to find enough data - local Tweets, blog posts and Facebook posts - to function.</p>

			<p>An upcoming project in Hounslow with the RSA has identified approximately 50 sources of useful online community data for that borough. </p>

			<p>In the context of the office, the current CX 'bucket' project is finding at least enough data to power a working prototype.</p>

			<!-- step 2 --> 
			<h3 id='step_2'>2 - Ambient displays driving social awareness.</h3>

			<p>There is a great deal of literature discussing relevantly similar design interventions. The appendix contains a table of the different research categories of similar work so far considered, using the following categories: </p>

			<ul> 
				<li>Desktop / Laptop</li>
				<li>Mobile</li>
				<li>Public display (broadcast)</li>
				<li>Public display (interactive)</li>
				<li>Other (no screen involved)</li>
			</ul>	 


			<p>One similar project is the Wray Display (Taylor & Cheverst, 2012), where users submitted photos online which then became visible in a public display in a shop in a the small village of Wray.</p>

			<p>Over three years a library of 1500 photos built up, with 14,000 photo views. Anecdotal evidence presented in the paper suggests that the display triggered conversations within the community and also helped new residents feel more embedded.</p>

			<p>In the office context, the "What's Happening" project at Georgia Institute of Technology (Zhao & Stasko, 2002) sought create a sense of community between the three different buildings of the Computer Studies Department.</p> 

			<p>
				They gathered data from internal mailing lists and  University event listings, as well as instant message chat, and presented it in a variety of different ways. 

				Staff could install an app that showed the data in a small box at the corner of their screen and when their computer went into screen saver.
			
				Public screens also displayed the data. 
			</P>

			<p>Using survey methods, the paper shows that users who installed the app were more aware of university events, were more familiar with research being undertaken by other groups and had a greater sense of community.</p>


			<p>Overall, few papers are available investigating using pre-existing social media activity and ambient displays.</p>


			<!-- step 3 --> 
			<h3 id='step_3'>3 - connection between community-focused digital activity and social capital.</h3>

			<p>Due to the breadth of social capital as a concept I will be seeking to find narrower constructs, however existing work on social capital offers useful context.</p>

			<p>There is evidence of a convincing link between digital activity and social capital, in particular looking at the effects of social networking sites.</p>

			<p>Much of the research finds that bridging social capital is implicated in social networking activity, while bonding social capital is not. Bridging social capital is taken between different groups, while bonding is within homogeneous groups.</p> 

			<p>Most research tends to agree that bridging social capital is more correlated with social media use than bonding social capital.</p>

			<p>Perhaps the most prominent paper is "The benefits of Facebook 'friends:' Social capital and college students' use of online social network sites" (Ellison et al, 2007), which has been cited nearly 3,500 times. In this paper they find that intensity of Facebook use is one of the strongest correlates of social capital, as determined by a standardised survey instrument.</p>

			<p>One of the most convincing papers on the topic (Burke, 2011) used Facebook adverts to gather approximately 1000 participants from across the English speaking world. Using privileged access to Facebook data (one of the authors is a Facebook employee), they were able to determine three modalities of Facebook use: direct messaging, passive consumption and broadcasting. Using two surveys of social capital taken one year apart they were then able to investigate correlations between changes in social capital and modalities of Facebook use, and found that all types of Facebook use, but especially direct messaging, correlate with bridging social capital (but no correlation with boding social capital).</p>

			<p>An interesting aspect of this paper is that they also gathered data on major life events. This enabled them to qualify the scale of the effects, for example:</p>

			<blockquote>"When compared to events, where we would expect a major disruption in social capital, the effect of Facebook is clear: every doubling of directed communication is associated with the same gain as moving to a new city, and about half the change incurred by job loss."</blockquote>

			<p>In addition, they were able to determine the direction of causality by using life events as a control. Losing a job causes a decrease in social capital, but not a change in Facebook behavior. This suggests that Facebook behaviour causes changes in social capital, not the other way round.</p>

			<p>This effect has also been observed on Twitter (Hofer & Aubert, 2013) and on a hyperlocal community website (Littau, 2007).</p>




			<!-- step 4 --> 
			<h3 id='step_4'>Step 4 - beneficial effects of social capital</h3>

			<p>As previously stated, social capital is likely to be too broad to be used as a basis for investigation or measurement. However, other proposed measurements, such as group effectiveness, are seen as subsets of social capital, so the construct bears further examination here. </p>

			<p>Social capital has attractive properties as an evaluative framework for an investigation of the potential impact of ambient computing on communities.</p>  

			<p>While there are many formulations of it, there is a consensus that all of them relate to "a resource to collective action" (Stone, 2001).</p>

			<p>The ONS publishes data about social capital, and has a question bank for quantitative purposes. The OECD provides an official definition which is accepted by the world bank and the ONS: </p>

			<blockquote>Networks together with shared norms, values and understandings that facilitate co-operation within or among groups.</blockquote>

			<p>Measurement of social capital and its dimensions has been discussed at length in the literature. As examined later, there is a well established link between social capital, especially the 'bridging' dimension, and broader social effects, such as wellbeing and health. Additionally there is a nascent body of work connecting digital activity to social capital.</p> 

			<p>The Strength of Weak Ties (Granovetter, 1973) is referenced across widely in both ambient computing and the previously discussed analysis of social networks. </p>

			<p>His work suggests there are strong benefits to increases in social capital, and in particular, as the title suggests, in large collections of weak ties.</p>

			<p>Stone gives a survey positive correlations with social capital that have been observed:</p>

			<blockquote>life expectancy; health status; suicide rates; teenage pregnancy; crime rates; participation rates in tertiary education; employment and unemployment rates; family income; marital relationship formations and dissolutions; business confidence; job growth; growth in GDP; and balance of trade</blockquote>

