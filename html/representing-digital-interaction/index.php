            <p>I'm doing a <a href='http://thecreativeexchange.org/'>PhD at RCA</a>. I'm investigating representations of social digital activity.</p>
            
            <h2 id='hypothesis'>Hypothesis</h2>
            
            <p>Most communities, for example a village or work place, produce a stream of 'digital exhaust' on services such as:</p>
            
            <ul>
                <li>Facebook &amp; Twitter  &amp; Tumblr</li>
                <li>Forums &amp; Blogs</li>
                <li>Dropbox &amp; Google Docs</li>                
            </ul>
            
            <p>My hypothesis is this data can be aggregated, processed and beneficially presented back to the community. In particular, I believe that it can help these types of goal-oriented communities collaborate better.</p>
            
            <p>There are three specific circumstances where I believe there is room for improvement:</p>

            <ul>
                <li><strong>Discovery:</strong> eg. printed highlights of hyperlocal blogs can reach people who do not read blogs online</li>
                <li><strong>Motivation:</strong> eg. <a href='http://en.wikipedia.org/wiki/Calm_technology'>Sonification</a> of coworkers Tweets might nudge the team towards knowing more about what each other are thinking about.</li>
                <li><strong>Aggregation:</strong> A pattern might emerge from combining Twitter with Forums that is not otherwise visible</li>                
            </ul>            
            
            
            
 
            <h2 id='analogy'>An analogy</h2>
            <p>Architects plan offices to increase collaboration, or town planners seek to foster community - is there an equivalent intervention in digital space?</p>
            
         
            <h2 id='testing'>Testing new kinds of interface</h2>
            
            <p>My intention is to carry out rapid, iterative testing of different methods of aggregation, filtering and presentation. In order to achieve this I have built a <a href='https://github.com/jimmytidey/obs'>custom software platform</a> for aggregation.</p>  
            

            <p>I'm particularly keen to get away from laptop - the omni-present interface - and experiment with more naturally shared media.</p>  

            
