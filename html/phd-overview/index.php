            <p>I'm doing a <a href='http://thecreativeexchange.org/'>PhD at RCA</a>. I'm investigating new ways of representing online social interactions.</p>
            
            <h2 id='hypothesis'>Research Question</h2>
            
            <p>Most communities, for example a village or work place, produce a stream of 'digital exhaust' on services such as:</p>
            
            <ul>
                <li>Facebook &amp; Twitter  &amp; Tumblr</li>
                <li>Forums &amp; Blogs</li>
                <li>Dropbox &amp; Google Docs</li>                
            </ul>
            
            <p>My hypothesis is this data can be aggregated, processed and presented back to the community to assist in collaboration and decision making.</p>
            
            <p>There are three specific areas I will be looking at:</p>

            <ul>
                <li><strong>Discovery:</strong> eg. printed highlights of hyperlocal blogs can reach people who do not read blogs online</li>
                <li><strong>Motivation:</strong> eg. <a href='http://en.wikipedia.org/wiki/Calm_technology'>Sonification</a> of coworkers Tweets might nudge the team towards knowing more about what each other are thinking about</li>
                <li><strong>Aggregation:</strong> A pattern might emerge from combining Twitter with blog posts that is otherwise invisible</li>                
            </ul>            
            
            <h2 id='analogy'>An analogy</h2>
            
            <p>Architects plan offices to increase collaboration, or town planners seek to foster community by structuring public space - is there an equivalent intervention in digital space?</p>
            
            <p>My projects will explore building interfaces that cultivate cohesion in exactly the same way.</p>
            
            
            <h2 id='prototyping'>Prototyping interfaces</h2>
            
            <p>My intention is to carry out rapid, iterative testing of different methods of aggregation, filtering and presentation. In order to achieve this I have built a flexible <a href='https://github.com/jimmytidey/obs'>software platform</a> for aggregation.</p>  

            <p>At the moment I'm developing a series of rapidly deployable ways to present the aggregated data.</p>

            <p>The personal computer screen is the omni-present interface for all digital activities - from organising your calendar to writing to social activity. </p>  
            
            <img src='img/three_activities.png'  alt='Diagram of a single person using a single monitor to do three distinct digital activities' class='center' />
                        
            <p>I'm particularly interested in getting away from this paradigm, instead using a variety of intentionally public or shared interfaces, for example print, projection, shared screens. </p>
            
            <img src='img/three_interface_three_person.png' alt='diagram of multiple people using multiple interfaces to see social digital activity' class='center' />
                        
            
